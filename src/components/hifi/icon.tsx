/** Renders a hi-fi icon by name. Size/colour come from the container
 * (`[&>svg]:size-*` + `text-*`); falls back to the building glyph. */
export function HiIcon({
  name,
  className,
}: {
  name: string
  className?: string
}) {
  return (
    <svg
      width="24"
      height="24"
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={1.75}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      aria-hidden="true"
      dangerouslySetInnerHTML={{ __html: PATHS[name] ?? PATHS.building }}
    />
  )
}
